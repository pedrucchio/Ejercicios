CREATE TABLE Books (
	book_id INTEGER PRIMARY KEY AUTOINCREMENT,
  	title varchar(20),
  	author varchar(20),
  	genre TEXT varchar(20),
  	published_year INTEGER,
   	isbn varchar(20),
  	price DECIMAL(10,2),
  	rating DECIMAL(2,2),
  	stock_count INTEGER
);

 
